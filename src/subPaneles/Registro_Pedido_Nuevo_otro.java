    public void obtenerpreciofinal(){
    
        //anda bien el contador
        
    int contadorP=tabladecomida.getRowCount();
    int i = 0;
     String cantidad = "";
    float PrecioTotal = 0 ;
    String Precio="";
    String precioCadete="";
    Float precioC=null;
    while(i<contadorP){
                Precio = tabladecomida.getValueAt(i, 3).toString();
                cantidad = tabladecomida.getValueAt(i, 2).toString();

                float pre = Float.parseFloat(Precio);

                int cant = Integer.parseInt(cantidad);

                PrecioTotal = PrecioTotal+(pre * cant);
    
        i++;
    }
    
    precioCadete=Final_Precio_P1.getText().toString();
    precioC=Float.parseFloat(precioCadete);
    PrecioTotal=PrecioTotal+precioC;
    Final_Precio_P.setText(Float.toString(PrecioTotal));
   
    }
